const char* LedCGIhandler(int iIndex, int iNumParams, char *pcParam[],
char *pcValue[])
{

	uint32_t i = 0;

//	if (iIndex == 0)
//	{
//		//turning the LED lights off
//		HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_RESET);
//		HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
//		HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);
//	}

	for (i = 0; i < iNumParams; i++)
	{
		if (strcmp(pcParam[i], "led") == 0)
		{

			if (strcmp(pcValue[i], "1") == 0)
			{
				HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_SET);
			}

			else if (strcmp(pcValue[i], "2") == 0)
			{
				HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
			}

			else if (strcmp(pcValue[i], "3") == 0)
			{
				HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);
			}
			else if (strcmp(pcValue[i], "4") == 0)
			{
				//turning the LED lights off
				HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_RESET);
				HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
				HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);
			}

		}

		else if (strcmp(pcParam[i], "frequencia") == 0)
		{
			if (strcmp(pcValue[i], "1") == 0)
			{
				setPWM(htim3, TIM_CHANNEL_2, 15341, 7670); //440Hz
			}

			else if (strcmp(pcValue[i], "2") == 0)
			{

				setPWM(htim3, TIM_CHANNEL_2, 25000, 7670); //440Hz

			}
			else if (strcmp(pcValue[i], "3") == 0)
			{

				setPWM(htim3, TIM_CHANNEL_2, 15341, 0); //0Hz

			}
		}

	}

	// the extension .shtml for SSI to work
	return "/index.html";

} // END [= CGI #5 =]

